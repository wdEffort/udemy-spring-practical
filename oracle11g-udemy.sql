-- 카드결제 테이블 생성
CREATE TABLE CARD
(
    USER_NAME VARCHAR2(20),
    AMOUNT    NUMBER(3)
);

-- 카드결제 테이블 Comment 설정
COMMENT ON TABLE CARD IS '카드결제';

-- 카드결제 테이블 컬럼 Comment 설정
COMMENT ON COLUMN CARD.USER_NAME IS '결제자';
COMMENT ON COLUMN CARD.AMOUNT IS '금액';


-- 티켓 테이블 생성
CREATE TABLE TICKET
(
    USER_NAME VARCHAR2(20),
    CNT       NUMBER(3)
        CONSTRAINT CNT_CK CHECK ( CNT < 6 )
);

-- 티켓 테이블 Comment 설정
COMMENT ON TABLE TICKET IS '티켓';

-- 티켓 테이블 컬럼 Comment 설정
COMMENT ON COLUMN TICKET.USER_NAME IS '티켓 구매자';
COMMENT ON COLUMN TICKET.CNT IS '티켓 수';

COMMIT;


-- 회원 테이블 생성
CREATE TABLE MEMBER
(
    ID    VARCHAR2(20) NOT NULL PRIMARY KEY,
    PWD   VARCHAR2(20) NOT NULL,
    NAME  VARCHAR2(20) NOT NULL,
    TEL   VARCHAR2(13) NOT NULL,
    EMAIL VARCHAR(100) NOT NULL
);

-- 회원 테이블 Comment 설정
COMMENT ON TABLE MEMBER IS '회원';

-- 회원 테이블 컬럼 Comment 설정
COMMENT ON COLUMN MEMBER.ID IS '아이디';
COMMENT ON COLUMN MEMBER.PWD IS '비밀번호';
COMMENT ON COLUMN MEMBER.NAME IS '이름';
COMMENT ON COLUMN MEMBER.TEL IS '연락처';
COMMENT ON COLUMN MEMBER.EMAIL IS '이메일';

COMMIT;